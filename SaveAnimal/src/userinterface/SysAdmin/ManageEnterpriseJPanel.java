    private void ManagejButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ManagejButtonActionPerformed
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(null,"Please select a row!", "get Request",JOptionPane.ERROR_MESSAGE);
        } else{
            //需要加入双重确认，并且改成以ID为搜索指引
            String name = jTable1.getValueAt(row,1).toString();
            EnterpriseDirectory worklist= network.getEnterpriseDirectory();
            Enterprise enterprise = worklist.findEnterprise(name);
                   
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            userProcessContainer.add(new ManageDepartmentJPanel(userProcessContainer,system,network , enterprise));
            layout.next(userProcessContainer);
        }     
       
    }//GEN-LAST:event_ManagejButtonActionPerformed

