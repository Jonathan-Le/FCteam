     public void populateRequestTable(){

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);      
            EnterpriseDirectory worklist= network.getEnterpriseDirectory();
          
            if (worklist != null){  
                for(Enterprise request : worklist.getEnterpriseList()){
                    Object[] row = new Object[2];
                    row[0]=request.getEnterpriseID();
                    row[1]=request.getEnterpriseName();
//                    row[2]=request.getEnterpriseType();
                    dtm.addRow(row);
                }
            }     
    }
