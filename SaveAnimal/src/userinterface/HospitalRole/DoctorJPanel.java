public void populateEuTable(){

            DefaultTableModel dtm = (DefaultTableModel) euRequestjTable.getModel();
            dtm.setRowCount(0);  
            ArrayList<WorkRequest> worklist =userEnterprise.getWorkQueue().getWorkRequestList();          
            if (worklist != null){               
                for(WorkRequest apl : worklist){
                    if (apl.getRequestType().equals("EuthanasiaRequest")) {
                        Object[] row = new Object[8];
                    row[0]= apl.getID();
                    row[1]= apl.getTitle();
                    row[2]= apl.getSender();
                    row[3]=apl.getReceiver();
                    row[4]=apl.getRequestDate();
                    row[5]=apl.getResolveDate();
                    row[6]=apl.getStatus();
                    row[7]=apl.getResult();
                    dtm.addRow(row);
                    }               
                }
            }    
    } 
